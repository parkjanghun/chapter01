package chapter01;

public class Arith {

	public static void main(String[] args) {
		int a=7;
		int b=3;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(10./3.); //.을 쓰면 double로
		
		//int 형과 double형의 사칙연산 결과는 double
		System.out.println(10./3);
		System.out.println((double)(10/3));
		
		//
	}

}
